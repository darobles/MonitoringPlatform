package cl.auter.patron.to;

/**
 *
 * @author drobles
 */
public class CodigoEnlaceTO {
    private int id_codigo;
    private String dominio;
    private String codigo1;
    private String codigo2;
    private String descripcion;

    public int getId_codigo() {
        return id_codigo;
    }

    public void setId_codigo(int id_codigo) {
        this.id_codigo = id_codigo;
    }

    public String getDominio() {
        return dominio;
    }

    public void setDominio(String dominio) {
        this.dominio = dominio;
    }

    public String getCodigo1() {
        return codigo1;
    }

    public void setCodigo1(String codigo1) {
        this.codigo1 = codigo1;
    }

    public String getCodigo2() {
        return codigo2;
    }

    public void setCodigo2(String codigo2) {
        this.codigo2 = codigo2;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    @Override
    public String toString() {
        return "CodigoEnlaceTO{" + "id_codigo=" + id_codigo + ", dominio=" + dominio + ", codigo1=" + codigo1 + ", codigo2=" + codigo2 + ", descripcion=" + descripcion + '}';
    }
    
    
}
